
public class Caculator {
	public int add(int num1 , int num2) {
		int num3 = num1 + num2;
		return num3;
	}
	public int sub(int num1 , int num2) {
		int num3 = num1 - num2;
		return num3;
	}
	public int div(int num1, int num2) {
		int num3 = num1/num2;
		return num3;
	}
	public int mul(int num1, int num2) {
		int num3 = num1* num2;
		return num3;
	}
}
